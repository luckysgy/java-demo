public class Source2 {
    private Long age;
    private String data;

    public Source2(Long age, String data) {
        this.age = age;
        this.data = data;
    }

    public Source2() {
    }

    public Long getAge() {
        return age;
    }

    public void setAge(Long age) {
        this.age = age;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
